    <h2> Apa Itu Framework ?</h2>
    <p> Framework merupakan kerangka kerja yang digunakan untuk mengembangkan aplikasi</br>
    berbasis dekstop maupun website. dengan menggunakan framework anda akan lebih mudah untuk </br>
    membuat aplikasi atau website.itu karena anda hanya perlu menyusun komponen - komponen</br>
    pemrograman yang sudah jadi. dengan kata lain, anda tidak perlu membuat berbagai fitur dari awal.</br>
    </p>
    <p>Framework sengaja diciptakan untuk membantu developer mengembangkan aplikasi atau website lebih cepat dan terstruktur</br>
    dengan begitu developer dan programmer tidak perlu melakukan kodingan program secara berulang - ulang.</br>

