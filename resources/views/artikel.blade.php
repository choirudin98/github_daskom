    <ol type="1">
    <li><h3>Program Menjadi Lebih Terstruktur dan Tersusun </h3> </li>
    <p>saat developer mengerjakan pengembangan aplikasi atau website yang besar, maka program</br>
    yang akan ditulis didalamnya menjadi semakin banyak. terkadang , semakin banyak program yang ditulis </br>
    akan menjadikan proses debugging semakin lambat. selain itu, saat anda mencari kode program yang error akan semakin sulit</br>
    karena program yang tidak terstruktur.</br>
